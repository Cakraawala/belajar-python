#operasi komparasi

# boolean
# >, <, >=, <=, ==, !=, is , is not

a = 4
b = 3
#lebih besar
hasil = a > b
print(a, '>', b, hasil)

#lebih kecil
hasil = a < b
print(a, '<', b, hasil)

#lebih besar sama dengan
hasil = a >= b
print(a, '>=', b, hasil)

#lebih kecil sama dengan
hasil = a <= b
print(a, '<=', b, hasil)

#sama dengan
hasil = a == b
print(a, '==', b, hasil)

#tidak sama dengan
hasil = a != b
print(a, '!=', b, hasil)

#x adalah y
hasil = a is b
print(a, 'is', b, hasil)

#x tidak adalah y
hasil = a is not b
print(a, 'is not', b, hasil)

hasil = a is b
print(a, 'is' , b,  hasil)

